var a =parseInt (prompt('Enter first number'));
var operator = prompt('Enter Operator +, -, *, /');
var b  =parseInt (prompt('Enter second number'));


 if (operator == '+' )
     {
    alert(a + b);
}else if (operator == '-' )
 {
    alert(a - b);
}else if (operator == '*' ) 
{
    alert(a * b);
}else if (operator == '/' ) {
    alert(a / b);
};



